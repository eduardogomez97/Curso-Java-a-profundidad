class OperadoresDeComparacion{
    public static void main(String[] args) {
        double sueldo1 = 1000.51558;
        double sueldo2 = 4500.999;
        System.out.println("Sueldo1 es mayor a sueldo2? "+(sueldo1>sueldo2));
        System.out.println("Sueldo1 es menor a sueldo2? "+(sueldo1<sueldo2));
        System.out.println("Sueldo1 es mayor o igual a sueldo2? "+(sueldo1>=sueldo2));
        System.out.println("Sueldo1 es menor o igual a sueldo2? "+(sueldo1<=sueldo2));
        System.out.println("Sueldo1 es diferente a sueldo2? "+(sueldo1!=sueldo2));
        System.out.println("Sueldo1 es el mismo que sueldo2? "+(sueldo1==sueldo2));
    }
}